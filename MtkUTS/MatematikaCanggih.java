package MtkUTS;

public class MatematikaCanggih {
    void pertambahan(int a, int b, int c){
        System.out.println("hasil pertambahan dari " + a + "+" + b + "+" + c + "="+(a+b+c));
    }
    void perkalian(int a, int b, int c){
        System.out.println("hasil perkalian dari " + a + "*" + b + "*" + c + "=" +(a*b*c));
    }
    void modulus(int a, int b, int c){
        System.out.println("hasil modulus dari " + a + "%" + b + "%" + c + "=" +(a%b%c));
    }
}