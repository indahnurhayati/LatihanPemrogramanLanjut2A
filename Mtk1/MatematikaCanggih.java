public class MatematikaCanggih {
    void pertambahan(int a, int b){
        System.out.println("hasil pertambahan dari " + a + "+" + b + "=" +(a+b));
    }
    void perkalian(int a, int b){
        System.out.println("hasil perkalian dari " + a + "*" + b + "=" +(a*b));
    }
    void modulus(int a, int b){
        System.out.println("hasil modulus dari " + a + "%" + b + "=" +(a%b));
    }
}
